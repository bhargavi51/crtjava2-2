import java.util.*;

public class MultiplicationTable
/* Multiplication table table using for loop*/

{
	
public static void main(String[] args) 
{
	
Scanner s=new Scanner(System.in);
	
int i,n;
	
n=s.nextInt();
	
for(i=1;i<=n;i++)
	
{
	    	
System.out.println(i+ "*" +n+"="+i*n);
	
}

	
}

}
